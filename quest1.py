banana = float(input("preço da fruta "))
maça = float(input("preço da segunda fruta "))

print(f"a soma das frutas é {banana+maça:.2f}")
print(f"a subtração das frutas é {banana-maça:.2f}")
print(f"a multiplicação das frutas é {banana*maça:.2f}")
print(f"a divisão das frutas é {banana/maça:.2f}")
print(f"o resto das frutas é {banana%maça:.2f}")
